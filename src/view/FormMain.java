        txtDevBrand.setText(dv.getBrand());
        txtModelDevice.setText(dv.getModel());
        txtSerieDev.setText(dv.getSerie_number());
        txtColorDev.setText(dv.getColor());
        txtProblemDev.setText(dv.getProblem());

